/* FILTROS */

function filtrar(){

    const text =
    buscador ?
    buscador.value.toLowerCase()
    : "";

    const cat =
    categoria ?
    categoria.value
    : "todos";

    const res = productos.filter(p=>{

        return (

            (cat === "todos" ||
            p.categoria === cat)

            &&

            (
            p.nombre.toLowerCase().includes(text)
            ||
            p.descripcion.toLowerCase().includes(text)
            )

        );

    });

    render(res);

}

if(buscador){
    buscador.addEventListener(
        "input",
        filtrar
    );
}

if(categoria){
    categoria.addEventListener(
        "change",
        filtrar
    );
}

/* MODAL */

function abrirModal(id){

    productoActual =
    productos.find(
        p => p.id === id
    );

    if(!productoActual) return;

    document.getElementById(
        "modalImage"
    ).src =
    productoActual.imagen;

    document.getElementById(
        "modalNombre"
    ).textContent =
    productoActual.nombre;

    document.getElementById(
        "modalDescripcion"
    ).textContent =
    productoActual.descripcion;

    document.getElementById(
        "cantidad"
    ).value = 1;

    document.getElementById(
        "modal"
    ).style.display = "flex";

}

function cerrarModal(e){

    const modal =
    document.getElementById(
        "modal"
    );

    if(
        !e ||
        e.target === modal ||
        e.target.id === "modal"
    ){
        modal.style.display = "none";
    }

}

function sumarCantidad(){

    const input =
    document.getElementById(
        "cantidad"
    );

    input.value =
    parseInt(input.value) + 1;

}

function restarCantidad(){

    const input =
    document.getElementById(
        "cantidad"
    );

    if(
        parseInt(input.value) > 1
    ){
        input.value =
        parseInt(input.value) - 1;
    }

}

/* CARRITO */

function agregarCarrito(){

    if(!productoActual) return;

    let carrito =
    JSON.parse(
        localStorage.getItem("carrito")
    ) || [];

    let cantidad =
    parseInt(
        document.getElementById(
            "cantidad"
        ).value
    );

    let existe =
    carrito.find(
        item =>
        item.id === productoActual.id
    );

    if(existe){

        existe.cantidad += cantidad;

    }else{

        carrito.push({

            id: productoActual.id,
            nombre: productoActual.nombre,
            descripcion: productoActual.descripcion,
            imagen: productoActual.imagen,
            categoria: productoActual.categoria,
            cantidad: cantidad

        });

    }

    localStorage.setItem(
        "carrito",
        JSON.stringify(carrito)
    );

    actualizarContador();

    cerrarModal();

    Swal.fire({
        icon: "success",
        title: "Producto agregado",
        text: productoActual.nombre,
        confirmButtonColor: "#c40000",
        timer: 1500,
        showConfirmButton: false
    });

}

function agregarRapido(id){

    let p =
    productos.find(
        x => x.id === id
    );

    if(!p) return;

    let carrito =
    JSON.parse(
        localStorage.getItem("carrito")
    ) || [];

    let existe =
    carrito.find(
        item =>
        item.id === p.id
    );

    if(existe){

        existe.cantidad++;

    }else{

        carrito.push({

            id: p.id,
            nombre: p.nombre,
            descripcion: p.descripcion,
            imagen: p.imagen,
            categoria: p.categoria,
            cantidad: 1

        });

    }

    localStorage.setItem(
        "carrito",
        JSON.stringify(carrito)
    );

    actualizarContador();

    Swal.fire({
        toast: true,
        position: "top-end",
        icon: "success",
        title: "Agregado al carrito",
        showConfirmButton: false,
        timer: 1200,
        timerProgressBar: true
    });

}

/* CONTADOR */

function actualizarContador(){

    let carrito =
    JSON.parse(
        localStorage.getItem("carrito")
    ) || [];

    let total =
    carrito.reduce(
        (a,b)=>a+b.cantidad,
        0
    );

    let badge =
    document.getElementById(
        "cartCount"
    );

    if(!badge) return;

    badge.textContent = total;

    badge.style.display =
    total > 0
    ? "flex"
    : "none";

}

actualizarContador();

