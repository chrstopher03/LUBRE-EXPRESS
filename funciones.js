/* PRODUCTOS */

function render(lista){

    if(!contenedor) return;

    contenedor.innerHTML = "";

    lista.forEach(p=>{

        contenedor.innerHTML += `
        <div class="card">

            <img src="${p.imagen}" alt="${p.nombre}">

            <div class="card-body">

                <h3>${p.nombre}</h3>

                <div class="card-buttons">

                    <button
                        class="btn-ver"
                        onclick="abrirModal(${p.id})">
                        Ver
                    </button>

                    <button
                        class="btn-cart"
                        onclick="agregarRapido(${p.id})">
                        Agregar
                    </button>

                </div>

            </div>

        </div>
        `;

    });

}

